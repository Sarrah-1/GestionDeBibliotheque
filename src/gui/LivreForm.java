    void loadLivre() {
        model.setRowCount(0);
        for (Livre l : ls.findAll()) {
            model.addRow(new Object[]{
                l.getTitre(), l.getAuteur(), l.getCategorie(), l.isDisponible() ? "Oui" : "Non"
            });
        }
    }

    private void loadEcategorie() {
        for (ECategorie ec : ECategorie.values()) {
            listCategorie.addItem(ec);
        }
    }


    private void btnRechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechercherActionPerformed
        
        String titrerecherche = txtRecherche.getText().trim();
        if(titrerecherche.isEmpty()){
            JOptionPane.showMessageDialog(this, "Veuillez entrer le titre");
            return;
        }
        List<Livre> livresTrouves = ls.rechercherLivreParTitre(titrerecherche);
        
        model.setRowCount(0);
        
        for(Livre livre : livresTrouves){
            model.addRow(new Object[]{
                livre.getTitre(),
                livre.getAuteur(),
                livre.getCategorie(),
                livre.isDisponible() ? "Oui" : "Non" 
            });
        }
        if (livresTrouves.isEmpty()){
            JOptionPane.showMessageDialog(this, "Aucun livre trouvé pour ce titre.");
        }
    }//GEN-LAST:event_btnRechercherActionPerformed



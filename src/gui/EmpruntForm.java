    void loadLivre() {
        for (Livre l : ls.findAll()) {
            ListeLivre.addItem(l);
        }
    }

    void loadEtudiant() {
        for (Etudiant e : es.findAll()) {
            ListeEtudiant.addItem(e);
        }
    }

    void loadStatu() {
        if (statutEmprunt == null) {
         System.out.println("statut Emprunt est null !");
         } else {
         statutEmprunt.removeAllItems();
         for (EStatut s : EStatut.values()) {
         ListeStatu.addItem(s.name());
         }
         }
        ListeStatu.removeAllItems();
        for (EStatut s : EStatut.values()) {
            ListeStatu.addItem(s.name());
        }

    }

    void load() {
        model.setRowCount(0);
        for (EmpruntLivre e : emps.findAll()) {
            if (e == null) {
                System.out.println("Avertissement : Un emprunt est null.");
                continue;
            }

            String titreLivre = (e.getLivre() != null) ? e.getLivre().getTitre() : "Livre inconnu";
            String nomEtudiant = (e.getEtudiant() != null) ? e.getEtudiant().getNom() : "Ã‰tudiant inconnu";

            model.addRow(new Object[]{
                titreLivre,
                nomEtudiant,
                e.getDateEmprunt(),
                e.getDateRetour(),
                e.getStatut()
            });
        }
    }

    void enregistrerEmprunt() {
        String selectedStatut = (String) statutEmprunt.getSelectedItem();
        est = EStatut.valueOf(selectedStatut);
    }


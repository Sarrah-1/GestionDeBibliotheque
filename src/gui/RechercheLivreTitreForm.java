    private void load() {
        List<Livre> livres = ls.findAll();
        model.setRowCount(0);
        for (Livre livre : livres) {
            model.addRow(new Object[]{
                livre.getTitre(),
                livre.getAuteur(),
                livre.getCategorie(),
                livre.isDisponible() ? "Oui" : "Non"
                
            });
        }

        if (livres.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Aucun livre trouvé");
        }
    }

